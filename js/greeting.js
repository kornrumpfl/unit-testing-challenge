export function greet(name) {
  switch(name){
    case "Martin":
      return "Howdy Coach!";
      break;
    case "Marc":
      return "Howdy Coach!";
      break;
    case "Ernst":
      return "Howdy Coach!";
      break;
    case "Dalia":
      return "Howdy Boss!";
      break;
    default:
      return "Howdy " + name + "!";
  }
  
}
